#ifdef RGB_MATRIX_ENABLE
// Forward-declare this helper function since it is defined in rgb_matrix.c.
void rgb_matrix_update_pwm_buffers(void);

// Disable lighting on boot (base layer requirement).
void keyboard_post_init_user(void) {
     rgb_matrix_disable_noeeprom();
}

// Global brightness (~10%). Use macro so it's compile-time and accessible everywhere.
#define LAYER_BRIGHTNESS 26

// Helper to set color with lowered brightness.
static inline void set_dim_hsv(uint8_t h, uint8_t s) {
     rgb_matrix_sethsv_noeeprom(h, s, LAYER_BRIGHTNESS);
}

// Assign a unique solid color to each non-base layer; base layer keeps lighting off.
layer_state_t layer_state_set_user(layer_state_t state) {
     uint8_t layer = get_highest_layer(state);

     if (layer == LAYER_BASE) {
          rgb_matrix_disable_noeeprom();
          return state;
     }

     rgb_matrix_enable_noeeprom();
     rgb_matrix_mode_noeeprom(RGB_MATRIX_NONE);

     switch (layer) {
          case LAYER_LOWER:   set_dim_hsv(170, 255); /* BLUE */      break;
          case LAYER_RAISE:   set_dim_hsv(213, 255); /* MAGENTA */   break;
          case LAYER_POINTER: set_dim_hsv( 85, 255); /* GREEN */     break;
          case LAYER_EXTRA1:  set_dim_hsv(  0, 255); /* RED */       break;
          case LAYER_EXTRA2:  set_dim_hsv( 43, 255); /* YELLOW */    break;
          case LAYER_EXTRA3:  set_dim_hsv(128, 255); /* CYAN */      break;
          case LAYER_EXTRA4:  set_dim_hsv( 28, 255); /* ORANGE */    break;
          default:            set_dim_hsv(  0,   0); /* WHITE (sat0)*/break;
     }

     return state;
}
#endif // RGB_MATRIX_ENABLE

#ifdef POINTING_DEVICE_ENABLE
// Implement drag-scroll: when DRGSCRL is held, convert Y motion to vertical wheel only.
static bool drag_scroll_active = false;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
     switch (keycode) {
          case DRGSCRL:
               drag_scroll_active = record->event.pressed;
               return false; // don't send keycode further
          default:
               return true;
     }
}

report_mouse_t pointing_device_task_user(report_mouse_t mouse_report) {
     if (drag_scroll_active) {
          // Vertical-only scroll: map Y to wheel and suppress cursor movement.
          mouse_report.v = (int8_t)ceilf((float)mouse_report.y / 4.0f);
          mouse_report.h = 0;
          mouse_report.x = 0;
          mouse_report.y = 0;
     }
     return mouse_report;
}
#endif // POINTING_DEVICE_ENABLE
